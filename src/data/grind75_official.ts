export const getStatistics = () => {
  const problems = grind75Data.problems;
  const difficultyStats = { easy: 0, medium: 0, hard: 0 };
  const topicStats: { [key: string]: number } = {};
  let totalMinutes = 0;

  problems.forEach(problem => {
    // Count by difficulty
    const difficulty = problem.difficulty.toLowerCase() as keyof typeof difficultyStats;
    difficultyStats[difficulty]++;

    // Sum time estimates
    totalMinutes += problem.timeEstimate || 30; // Default to 30 minutes if not provided

    // Count by topic
    problem.topics.forEach(topic => {
      topicStats[topic] = (topicStats[topic] || 0) + 1;
    });
  });

  return {
    totalProblems: problems.length,
    totalHours: Math.round(totalMinutes / 60),
    difficultyStats,
    topicStats
  };
};